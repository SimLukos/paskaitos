const sudetis = function (one, two) {
  const result = one + two;
  return result;
};

const atimtis = function (one, two) {
  const result = one - two;
  return result;
};

const dalyba = function (one, two) {
  const result = one / two;
  return result;
};

const daugyba = function (one, two) {
  const result = one * two;
  return result;
};

const liekana = function (one, two) {
  const result = one % two;
  return result;
};

console.log(sudetis(29, 1));
console.log(atimtis(29, 1));
console.log(dalyba(29, 1));
console.log(daugyba(29, 1));
console.log(liekana(29, 1));
